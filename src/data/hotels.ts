export interface PartnerLinks {
  agoda?: string;
  myrealtrip?: string;
  tripdotcom?: string;
}

export interface QuickSummary {
  recommendation: string; // 추천 여행자
  strength: string;       // 강점
  caution: string;        // 주의
  character: string;      // 성격
}

/**
 * 상세 페이지(HotelDetailTemplate)에 사용되는 콘텐츠.
 * 오사카·후쿠오카 등 모든 지역 호텔에 재사용 가능한 구조.
 */
export interface HotelDetail {
  code: string;              // OSA001, FUK001 등 — URL /hotel/[code] 에 사용
  badges: string[];          // 상세 페이지 상단 뱃지 목록
  summary: string;           // 핵심 한 줄 (리스트 selectionReason과 별개)
  quickSummary: QuickSummary;
  whyRecommended: string[];  // 왜 추천하나요?
  goodFor: string[];         // 이런 여행자에게 잘 맞아요
  notBestFor: string[];      // 이런 경우엔 다른 카드가 더 나을 수 있어요
  pros: string[];            // 핵심 요약 — 장점
  cons: string[];            // 핵심 요약 — 주의포인트
  areaContext: string[];     // 왜 이 지역에서 이 호텔이 강한가요?
  hukosakaVerdict: string;   // 후쿠오사카 한 줄 판단
}

/**
 * 리스트 카드(HotelCard)에 사용되는 기본 호텔 정보.
 * detail 필드가 있으면 상세 페이지 연결 가능.
 */
export interface Hotel {
  id: string;
  name: string;
  area: string;
  badge: string;
  badgeColor: "sky" | "indigo" | "violet" | "emerald" | "orange";
  summary: string;
  selectionReason: string;
  recommendedFor: string;
  caution: string;
  primaryThemes: string[];
  tags: string[];
  partnerLinks?: PartnerLinks;
  detail?: HotelDetail;
}

